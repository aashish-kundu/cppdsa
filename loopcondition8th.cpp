#include <iostream>
using namespace std;

int main()
{

    // condition gived in for loop it will print Aashish kundu with the table of five

    int i;

    for (i = 0; i <= 50; i = i + 5)
    {
        cout << "Aashish Kundu" << i << endl;
    }

    // print the value of i
    cout << i << endl;

    return 0;
}